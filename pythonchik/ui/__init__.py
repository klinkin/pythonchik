"""Пользовательский интерфейс приложения Pythonchik.

Этот модуль предоставляет компоненты пользовательского интерфейса, построенные
на основе библиотеки CustomTkinter. Интерфейс поддерживает современный дизайн,
темную и светлую темы, а также адаптивное размещение компонентов.

Основные компоненты:
- ModernApp: Главный класс приложения, который инициализирует окно
- Специализированные фреймы:
  - SideBarFrame: Боковая панель навигации с кнопками
  - ActionMenuFrame: Панель действий и операций
  - ResultFrame: Фрейм для отображения результатов операций
  - LogFrame: Отображение журнала действий и сообщений

Особенности:
- Компонентная архитектура интерфейса с повторным использованием элементов
- Реактивные обновления через систему событий
- Адаптивная компоновка, поддерживающая различные размеры экрана
- Поддержка темной и светлой тем оформления
- Локализация на русском языке

Пример использования:
    >>> from pythonchik.core.application_core import ApplicationCore
    >>> from pythonchik.events.eventbus import EventBus
    >>> from pythonchik.ui.app import ModernApp
    >>>
    >>> # Создание ядра и шины событий
    >>> bus = EventBus()
    >>> core = ApplicationCore(bus)
    >>>
    >>> # Инициализация и запуск приложения
    >>> app = ModernApp(core, event_bus=bus)
    >>> app.mainloop()
"""
